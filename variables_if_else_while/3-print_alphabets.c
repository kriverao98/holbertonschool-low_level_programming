#include <stdio.h>
/**
 * main - A simple program that prints out the alphabet in
 * lowercase and uppercase followed by a newline.
 * Return: Always 0 (Success)
 */

int main(void)
{
	char az;
{
	for (az = 'a'; az <= 'z'; az++);
	putchar(az);
	{
	for (az = 'A'; az <= 'Z'; az++);
	putchar(az);
	}
	{
		putchar('\n');
	}
}
	return (0);
}
