#include <stdio.h>
/**
 * main - A simple program that prints out the alphabet in lowercase and uppercase
 * followed by a newline.
 * Return: Always 0 (Success)
 */

int main(void)
{
	char az = 'a';
	char ch = 'A';
{
	for (az = 'a'; az <= 'z'; az++);
	putchar(az);
	{
		for (ch = 'A'; ch <= 'Z'; ch++);
		putchar(ch);
	}
	{
		putchar('\n');
	}
}
	return (0);
}
